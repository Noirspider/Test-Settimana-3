/* ESERCIZIO 18
  Scrivi una funzione chiamata "searchAndDivide" che riceve una stringa come parametro e ritorna un oggetto contenente due array: "match" e "unmatch".
  "match" deve includere tutti i film dell'array "movies" fornito che contengono la stringa fornita all'interno del proprio titolo, mentre "unmatch" deve includere tutti i rimanenti.
*/
function searchAndDivide(searchString, movieArray) {
  const searchLower = searchString.toLowerCase();

  const matchMovies = movieArray.filter(movie =>
    movie.Title.toLowerCase().includes(searchLower)
  );

  const unmatchMovies = movieArray.filter(movie =>
    !movie.Title.toLowerCase().includes(searchLower)
  );

  return {
    match: matchMovies,
    unmatch: unmatchMovies
  };
}

const matchResult = searchAndDivide("lord", movies);
console.log("18) Film corrispondenti: " + matchResult.match);
console.log("18) Film non corrispondenti: " + matchResult.unmatch);

